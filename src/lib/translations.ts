export function getUITranslation(
  language: Language,
  section: keyof typeof siteTranslations.en,
  key: string
): string {
  const translations = siteTranslations[language] || siteTranslations.en;
  const sectionTranslations = translations[section] as Record<string, string>;
  const fallbackSection = siteTranslations.en[section] as Record<string, string>;
  return sectionTranslations?.[key] || fallbackSection?.[key] || key;
}
